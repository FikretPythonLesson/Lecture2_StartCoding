import turtle

# Square 
# ------
turtle.shape("turtle")
turtle.pensize(4)
turtle.bgcolor("yellow")

turtle.color("blue", "red")
turtle.begin_fill()

turtle.forward(125)
turtle.left(90)
turtle.forward(125)
turtle.left(90)
turtle.forward(125)
turtle.left(90)
turtle.forward(125)
turtle.left(90)

turtle.end_fill()

turtle.done()
